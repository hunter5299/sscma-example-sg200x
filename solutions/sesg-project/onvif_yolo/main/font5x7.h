// 取字符的列位图；未知字符回退为空格。大小写统一为大写。
inline const uint8_t* columns(char c) {
    if (c >= 'a' && c <= 'z') c = static_cast<char>(c - 'a' + 'A');
    for (const auto& g : kGlyphs) {
        if (g.c == c) return g.col;
    }
    return kGlyphs[0].col;  // space
}

